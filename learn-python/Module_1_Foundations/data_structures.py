# Example: Built-in Data Structures
# Demonstrates operations on list, tuple, dict, and set

# List
fruits = ["apple", "banana", "cherry"]
fruits.append("date")
print("List:", fruits)

# Tuple
tuple_example = (1, 2, 3)
print("Tuple:", tuple_example)

# Dictionary
dict_example = {"name": "AI Engineer", "age": 30}
dict_example["role"] = "Developer"
print("Dictionary:", dict_example)

# Set
set_example = {1, 2, 3}
set_example.add(4)
print("Set:", set_example)
