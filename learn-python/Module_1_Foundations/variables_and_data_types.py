# Example: Variables and Data Types
# Demonstrates basic data types and variable usage in Python

# Integer
x = 10
print(f"Integer: {x}")

# Float
y = 3.14
print(f"Float: {y}")

# String
name = "AI Engineer"
print(f"String: {name}")

# Boolean
is_active = True
print(f"Boolean: {is_active}")

# None
default_value = None
print(f"None: {default_value}")