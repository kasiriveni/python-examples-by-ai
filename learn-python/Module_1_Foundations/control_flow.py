# Example: Control Flow
# Demonstrates if/else and loops in Python

# If/else example
number = 10
if number > 0:
    print("Positive number")
elif number == 0:
    print("Zero")
else:
    print("Negative number")

# For loop example
for i in range(5):
    print(f"Iteration {i}")

# While loop example
count = 0
while count < 3:
    print(f"Count: {count}")
    count += 1
