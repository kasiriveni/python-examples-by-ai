# 1. pathlib patterns
# ═══════════════════════════════════════════
def pathlib_tour(base: Path):
    print("=== pathlib tour ===")
    print(f"  parts:   {base.parts}")
    print(f"  name:    {base.name}")
    print(f"  stem:    {base.stem}")
    print(f"  suffix:  {base.suffix}")
    print(f"  parent:  {base.parent}")
    print(f"  exists:  {base.exists()}")

    # Build paths
    data_dir = base / "data"
    data_dir.mkdir(exist_ok=True)
    readme = data_dir / "README.md"
    readme.write_text("# Demo\nGenerated by pathlib_tour.")

    print(f"  created: {readme}")
    print(f"  size:    {readme.stat().st_size} bytes")

    # Glob patterns
    py_files = list(base.glob("*.py"))
    print(f"  .py files in base: {len(py_files)}")

    all_py = list(base.rglob("*.py"))
    print(f"  .py files (recursive): {len(all_py)}")

    # Resolve absolute path
    print(f"  absolute: {readme.resolve()}")

    # Cleanup
    shutil.rmtree(data_dir)

