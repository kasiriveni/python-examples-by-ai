# 3. CSV handling
# ═══════════════════════════════════════════
def write_csv(path: Path, rows: list[dict]) -> None:
    if not rows: return
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=rows[0].keys())
        writer.writeheader()
        writer.writerows(rows)

def read_csv(path: Path) -> list[dict]:
    with path.open(encoding="utf-8") as fh:
        return list(csv.DictReader(fh))

def csv_to_json(csv_path: Path, json_path: Path, indent: int = 2) -> None:
    rows = read_csv(csv_path)
    json_path.write_text(json.dumps(rows, indent=indent), encoding="utf-8")

class CSVProcessor:
    """Stream-process a CSV file without loading it all into memory."""

    def __init__(self, path: Path):
        self.path = path

    def filter_rows(self, pred) -> Generator[dict, None, None]:
        with self.path.open(encoding="utf-8") as fh:
            for row in csv.DictReader(fh):
                if pred(row):
                    yield row

    def aggregate(self, key_col: str, value_col: str) -> dict[str, float]:
        totals: dict[str, float] = {}
        with self.path.open(encoding="utf-8") as fh:
            for row in csv.DictReader(fh):
                k = row[key_col]
                totals[k] = totals.get(k, 0.0) + float(row[value_col])
        return totals

# ═══════════════════════════════════════════
# 4. JSON patterns
# ═══════════════════════════════════════════
def read_json_file(path: Path) -> dict | list:
    return json.loads(path.read_text(encoding="utf-8"))

def write_json_file(path: Path, data, indent: int = 2) -> None:
    path.write_text(json.dumps(data, indent=indent, ensure_ascii=False), encoding="utf-8")

def stream_ndjson(path: Path) -> Iterator[dict]:
    """Stream newline-delimited JSON (NDJSON)."""
    with path.open(encoding="utf-8") as fh:
        for line in fh:
            line = line.strip()
            if line:
                yield json.loads(line)

def write_ndjson(path: Path, records: list[dict]) -> None:
    with path.open("w", encoding="utf-8") as fh:
        for record in records:
            fh.write(json.dumps(record) + "\n")

