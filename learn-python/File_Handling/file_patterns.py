if __name__ == "__main__":
    import tempfile, os

    with tempfile.TemporaryDirectory() as tmp_dir:
        base = Path(tmp_dir)

        # CSV
        print("=== CSV ===")
        employees = [
            {"name":"Alice",  "dept":"Eng",   "salary":"90000"},
            {"name":"Bob",    "dept":"Eng",   "salary":"85000"},
            {"name":"Carol",  "dept":"Sales", "salary":"70000"},
            {"name":"Dave",   "dept":"Sales", "salary":"72000"},
            {"name":"Eve",    "dept":"HR",    "salary":"65000"},
        ]
        csv_path = base / "employees.csv"
        write_csv(csv_path, employees)
        rows = read_csv(csv_path)
        print(f"  Read {len(rows)} rows from CSV")

        proc = CSVProcessor(csv_path)
        eng = list(proc.filter_rows(lambda r: r["dept"] == "Eng"))
        print(f"  Engineering: {[r['name'] for r in eng]}")
        dept_totals = proc.aggregate("dept", "salary")
        for dept, total in sorted(dept_totals.items()):
            print(f"  {dept}: ${total:,.0f}")

        # JSON
        print("\n=== JSON ===")
        json_path = base / "data.json"
        write_json_file(json_path, employees)
        loaded = read_json_file(json_path)
        print(f"  Written and read back {len(loaded)} records")

        ndjson_path = base / "events.ndjson"
        write_ndjson(ndjson_path, [{"event":"login","user":"alice"},
                                   {"event":"query","user":"bob"}])
        events = list(stream_ndjson(ndjson_path))
        print(f"  NDJSON: {events}")

        # Binary
        print("\n=== Binary ===")
        bin_path = base / "records.bin"
        original = [(1, 98.7, "Alice"), (2, 87.3, "Bob"), (3, 95.1, "Carol")]
        write_binary(bin_path, original)
        loaded_bin = read_binary(bin_path)
        print(f"  Written: {original}")
        print(f"  Read:    {loaded_bin}")
        assert original == loaded_bin

        # Checksum
        print("\n=== Checksum ===")
        checksum = sha256_file(csv_path)
        print(f"  SHA-256 of CSV: {checksum[:16]}...")

        # Atomic write
        target = base / "config.json"
        atomic_write(target, '{"debug": false}')
        print(f"  Atomic write: {target.read_text()}")

        # Line / word count
        print("\n=== Stream counts ===")
        lines = line_count(csv_path)
        print(f"  Lines in CSV (incl. header): {lines}")

        find_results = find_in_file(csv_path, "eng")
        print(f"  Lines with 'eng': {len(find_results)}")

        # pathlib tour
        pathlib_tour(base)
