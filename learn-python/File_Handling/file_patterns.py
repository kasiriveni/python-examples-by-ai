# 2. Streaming large text files
# ═══════════════════════════════════════════
def stream_lines(path: Path, encoding: str = "utf-8") -> Iterator[str]:
    """Stream lines without loading entire file into memory."""
    with path.open(encoding=encoding) as fh:
        for line in fh:
            yield line.rstrip("\n")

def word_count(path: Path) -> dict[str, int]:
    """Count word frequencies streaming a file."""
    counts: dict[str, int] = {}
    for line in stream_lines(path):
        for word in line.split():
            word = word.strip(".,!?;:\"'").lower()
            if word:
                counts[word] = counts.get(word, 0) + 1
    return counts

def line_count(path: Path) -> int:
    """Count lines efficiently with read()."""
    with path.open("rb") as fh:
        return sum(1 for _ in fh)

def find_in_file(path: Path, pattern: str) -> list[tuple[int, str]]:
    """Find all lines containing pattern, return (lineno, line)."""
    results = []
    for i, line in enumerate(stream_lines(path), 1):
        if pattern.casefold() in line.casefold():
            results.append((i, line))
    return results

