# 5. Binary file handling
# ═══════════════════════════════════════════
# Simple binary format: header (4 bytes magic, 4 bytes version, 4 bytes record_count)
MAGIC = b"SBIN"
VERSION = 1

def write_binary(path: Path, records: list[tuple[int, float, str]]) -> None:
    """Write binary file: header + records (int32, float64, 32-byte UTF-8 name)."""
    with path.open("wb") as fh:
        fh.write(struct.pack(">4sII", MAGIC, VERSION, len(records)))
        for record_id, score, name in records:
            name_bytes = name.encode("utf-8")[:32].ljust(32, b"\x00")
            fh.write(struct.pack(">Id32s", record_id, score, name_bytes))

def read_binary(path: Path) -> list[tuple[int, float, str]]:
    records = []
    with path.open("rb") as fh:
        magic, version, count = struct.unpack(">4sII", fh.read(12))
        assert magic == MAGIC, f"Bad magic: {magic!r}"
        for _ in range(count):
            record_id, score, name_bytes = struct.unpack(">Id32s", fh.read(44))
            records.append((record_id, score, name_bytes.rstrip(b"\x00").decode()))
    return records

# ═══════════════════════════════════════════
# 6. File checksum and safe write
# ═══════════════════════════════════════════
def sha256_file(path: Path, chunk_size: int = 65536) -> str:
    h = hashlib.sha256()
    with path.open("rb") as fh:
        while chunk := fh.read(chunk_size):
            h.update(chunk)
    return h.hexdigest()

def atomic_write(path: Path, content: str, encoding: str = "utf-8") -> None:
    """Write to a temp file, then rename (atomic on POSIX, best-effort on Windows)."""
    tmp = path.with_suffix(path.suffix + ".tmp")
    try:
        tmp.write_text(content, encoding=encoding)
        tmp.replace(path)
    except Exception:
        tmp.unlink(missing_ok=True)
        raise

def safe_copy(src: Path, dst: Path) -> None:
    """Copy with checksum verification."""
    shutil.copy2(src, dst)
    if sha256_file(src) != sha256_file(dst):
        dst.unlink()
        raise IOError(f"Checksum mismatch after copying {src} → {dst}")

