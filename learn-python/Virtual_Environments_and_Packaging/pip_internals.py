import subprocess
import sys
import json
from pathlib import Path

# ─────────────────────────────────────────────────────────
# 1. pip from Python
# ─────────────────────────────────────────────────────────
class PipManager:
    """Invoke pip programmatically as a subprocess."""

    def __init__(self, python: str | None = None):
        self.python = python or sys.executable

    def _run(self, args: list[str]) -> tuple[int, str, str]:
        cmd = [self.python, "-m", "pip"] + args
        result = subprocess.run(cmd, capture_output=True, text=True)
        return result.returncode, result.stdout, result.stderr

    def install(self, *packages: str, upgrade: bool = False,
                constraints: Path | None = None) -> bool:
        args = ["install"]
        if upgrade:
            args.append("--upgrade")
        if constraints:
            args += ["-c", str(constraints)]
        args += list(packages)
        code, out, err = self._run(args)
        if code != 0:
            print(f"pip install failed:\n{err}")
        return code == 0

    def uninstall(self, *packages: str) -> bool:
        code, _, err = self._run(["uninstall", "-y", *packages])
        return code == 0

    def list_packages(self) -> list[dict]:
        code, out, _ = self._run(["list", "--format=json"])
        if code != 0:
            return []
        return json.loads(out)

    def show(self, package: str) -> dict:
        code, out, _ = self._run(["show", package])
        if code != 0:
            return {}
        return dict(line.split(": ", 1) for line in out.strip().splitlines() if ": " in line)

    def freeze(self) -> list[str]:
        _, out, _ = self._run(["freeze"])
        return [line.strip() for line in out.splitlines() if line.strip()]

    def check(self) -> bool:
        """Check for dependency conflicts."""
        code, out, _ = self._run(["check"])
        print(out.strip() or "No conflicts.")
        return code == 0

    def outdated(self) -> list[dict]:
        _, out, _ = self._run(["list", "--outdated", "--format=json"])
        try:
            return json.loads(out)
        except json.JSONDecodeError:
            return []

