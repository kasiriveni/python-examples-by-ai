# 4. Dependency resolver (simplified)
# ─────────────────────────────────────────────────────────
class SimpleDependencyResolver:
    """Educational: understand how pip resolves dependencies (simplified)."""

    def __init__(self):
        self._packages: dict[str, dict] = {}  # {name: {version, deps}}

    def add(self, name: str, version: str, deps: list[str] | None = None):
        self._packages[name.lower()] = {"version": version, "deps": deps or []}

    def resolve(self, requirements: list[str]) -> dict[str, str]:
        """Return {package: version} for all transitive deps."""
        resolved: dict[str, str] = {}
        to_resolve = list(requirements)

        while to_resolve:
            req = to_resolve.pop(0).lower()
            if req in resolved:
                continue
            pkg = self._packages.get(req)
            if not pkg:
                print(f"  WARNING: '{req}' not in registry")
                continue
            resolved[req] = pkg["version"]
            for dep in pkg["deps"]:
                if dep.lower() not in resolved:
                    to_resolve.append(dep)

        return resolved

